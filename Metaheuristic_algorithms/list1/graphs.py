import matplotlib.pyplot as plt


def read_graph_file(f_name):
    data = []
    with open(f_name, "r") as f:
        input_line = ""
        while input_line.strip() != "NODE_COORD_SECTION":
            input_line = f.readline()
        for line in f:
            if line.strip() == "EOF":
                break
            input_data = line.strip().split(" ")
            data.append((int(input_data[1]), int(input_data[2])))
    return data


def generate_cycle_graph(vertices_coords, cycle, title):
    plt.title(title)
    for i in range(0, len(cycle) - 1):
        x_ax = [vertices_coords[cycle[i]][0], vertices_coords[cycle[i+1]][0]]
        y_ax = [vertices_coords[cycle[i]][1], vertices_coords[cycle[i+1]][1]]
        plt.plot(x_ax, y_ax, color="blue", linewidth=1)

    x_ax = [vertices_coords[cycle[0]][0], vertices_coords[cycle[-1]][0]]
    y_ax = [vertices_coords[cycle[0]][1], vertices_coords[cycle[-1]][1]]
    plt.plot(x_ax, y_ax, color="blue", linewidth=1)

    x_ax = [x[0] for x in vertices_coords]
    y_ax = [y[1] for y in vertices_coords]
    plt.scatter(x_ax, y_ax, s=8, c="red")

    plt.savefig(title)
    plt.clf()
    return


